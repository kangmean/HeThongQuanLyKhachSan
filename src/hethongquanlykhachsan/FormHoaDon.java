     private void btThemHoaDonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btThemHoaDonActionPerformed
          try {
               int maKH = Integer.parseInt(txtMaKH.getText());
               int maPhong = Integer.parseInt(txtMaDatPhong.getText());
               String ngayLap = txtNgayLap.getText().trim();
               double tongTien = Double.parseDouble(txtTongTien.getText());

               if (hoaDonDao.themHoaDon(maKH, maPhong, ngayLap, tongTien)) {
                    JOptionPane.showMessageDialog(this, "Thêm hóa đơn thành công!");
                    loadHoaDon();
                    btXoaTrangActionPerformed(null);
               }
           } catch (Exception e) {
               JOptionPane.showMessageDialog(this, "Lỗi thêm: " + e.getMessage());
           }
     }//GEN-LAST:event_btThemHoaDonActionPerformed

     private void tblHoaDonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblHoaDonMouseClicked
          int row = tblHoaDon.getSelectedRow();
          if (row >= 0) {
               txtMaHoaDon.setText(tblHoaDon.getValueAt(row, 0).toString());
               txtMaKH.setText(tblHoaDon.getValueAt(row, 1).toString());
               txtMaDatPhong.setText(tblHoaDon.getValueAt(row, 2).toString());
               txtNgayLap.setText(tblHoaDon.getValueAt(row, 3).toString());
               txtTongTien.setText(tblHoaDon.getValueAt(row, 4).toString());
          }
     }//GEN-LAST:event_tblHoaDonMouseClicked

