     private void btXoaHoaDonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btXoaHoaDonActionPerformed
          try {
               int maHD = Integer.parseInt(txtMaHoaDon.getText());
               if (JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn xóa?", "Xác nhận", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                    if (hoaDonDao.xoaHoaDon(maHD)) {
                         JOptionPane.showMessageDialog(this, "Xóa hóa đơn thành công!");
                         loadHoaDon();
                         btXoaTrangActionPerformed(null);
                    }
               }
           } catch (Exception e) {
               JOptionPane.showMessageDialog(this, "Lỗi xóa: " + e.getMessage());
           }
     }//GEN-LAST:event_btXoaHoaDonActionPerformed

     private void btSuaHoaDonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSuaHoaDonActionPerformed
          try {
               int maHD = Integer.parseInt(txtMaHoaDon.getText());
               int maKH = Integer.parseInt(txtMaKH.getText());
               int maPhong = Integer.parseInt(txtMaDatPhong.getText());
               String ngayLap = txtNgayLap.getText().trim();
               double tongTien = Double.parseDouble(txtTongTien.getText());

               if (hoaDonDao.suaHoaDon(maHD, maKH, maPhong, ngayLap, tongTien)) {
                    JOptionPane.showMessageDialog(this, "Sửa hóa đơn thành công!");
                    loadHoaDon();
               }
          } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Lỗi sửa: " + e.getMessage());
          }
     }//GEN-LAST:event_btSuaHoaDonActionPerformed

     private void btThoatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btThoatActionPerformed
          int confirm = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn thoát?", "Xác nhận thoát", JOptionPane.YES_NO_OPTION);
          if (confirm == JOptionPane.YES_OPTION) {
               dispose(); // Đóng cửa sổ hiện tại
          }
     }//GEN-LAST:event_btThoatActionPerformed

