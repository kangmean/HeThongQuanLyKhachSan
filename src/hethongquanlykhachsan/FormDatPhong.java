     private void loadDatPhong() {
          try {
              ResultSet rs = datPhongDao.getAllDatPhong();
              DefaultTableModel model = (DefaultTableModel) tblDatPhong.getModel();
              model.setRowCount(0); // xóa dữ liệu cũ
              while(rs.next()) {
                  model.addRow(new Object[]{
                      rs.getInt("MaDatPhong"),
                      rs.getInt("MaKH"),
                      rs.getInt("MaPhong"),
                      rs.getString("NgayDat"),
                      rs.getString("TrangThai"),
                      rs.getString("NgayNhan"),
                      rs.getString("NgayTra")
                  });
              }
          } catch(SQLException e) {
              JOptionPane.showMessageDialog(this, "Lỗi load dữ liệu: " + e.getMessage());
          }
      }
