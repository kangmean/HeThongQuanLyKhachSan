     private void btThemPhongActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btThemPhongActionPerformed
          try {
               int maPhong = Integer.parseInt(txtMaPhong.getText());
               int maKH = Integer.parseInt(txtMaKhachHang.getText());
               String ngayDat = txtNgayDat.getText().trim();
               String trangThai = cboTrangThai.getSelectedItem().toString();

               if(datPhongDao.themDatPhong(maPhong, maKH, ngayDat, "", "", trangThai)) {
                    JOptionPane.showMessageDialog(this, "Đặt phòng thành công!");
                    phongDao.capNhatTrangThai(maPhong, "Đã đặt");
                    loadDatPhong();
                    btXoaTrangActionPerformed(null);
               }
          } catch(Exception e) {
               JOptionPane.showMessageDialog(this, "Lỗi: " + e.getMessage());
          }
     }//GEN-LAST:event_btThemPhongActionPerformed

     private void btXoaPhongActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btXoaPhongActionPerformed
          try {
               int maDatPhong = Integer.parseInt(txtMaDatPhong.getText());
               int maPhong = Integer.parseInt(txtMaPhong.getText());

               int confirm = JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn xóa?", "Xác nhận", JOptionPane.YES_NO_OPTION);
               if(confirm == JOptionPane.YES_OPTION) {
                   if(datPhongDao.xoaDatPhong(maDatPhong)) {
                       JOptionPane.showMessageDialog(this, "Xóa thành công!");
                       phongDao.capNhatTrangThai(maPhong, "Trống");
                       loadDatPhong();
                       btXoaTrangActionPerformed(null);
                   }
               }
           } catch(Exception e) {
               JOptionPane.showMessageDialog(this, "Lỗi: " + e.getMessage());
           }
     }//GEN-LAST:event_btXoaPhongActionPerformed

     private void btSuaPhongActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSuaPhongActionPerformed
          try {
               int maDatPhong = Integer.parseInt(txtMaDatPhong.getText());
               int maPhong = Integer.parseInt(txtMaPhong.getText());
               int maKH = Integer.parseInt(txtMaKhachHang.getText());
               String ngayDat = txtNgayDat.getText().trim();
               String ngayNhan = txtNgayNhan.getText().trim();
               String ngayTra = txtNgayTra.getText().trim();
               String trangThai = cboTrangThai.getSelectedItem().toString();

               if(datPhongDao.suaDatPhong(maDatPhong, maPhong, maKH, ngayDat, ngayNhan, ngayTra, trangThai)) {
                   JOptionPane.showMessageDialog(this, "Cập nhật đặt phòng thành công!");
                   phongDao.capNhatTrangThai(maPhong, trangThai);
                   loadDatPhong();
                   btXoaTrangActionPerformed(null);
               }
           } catch(Exception e) {
               JOptionPane.showMessageDialog(this, "Lỗi: " + e.getMessage());
           }
     }//GEN-LAST:event_btSuaPhongActionPerformed

